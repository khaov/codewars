// Линейный вычислительный процесс. Вычисление заданной величины.
// Разработать линейную программу для вычисления заданной величины по определенной формуле.

// Вычислить площадь треугольника по формуле:
// S = 1/2 * a * h , где a - основание треугольника, h - высота, опущенная на это основание.

// Solution:

function area(a, h) {
  return a * h / 2;
}

// Sample Tests:

